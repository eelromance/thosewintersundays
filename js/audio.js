$(document).ready(function() {
    var obj = document.createElement("audio");
    obj.src="/aud/intro.wav";
    obj.volume=1;
    obj.autoPlay=false;
    obj.preLoad=true;

    $("#intro").click(function() {
        obj.play();
    });
});

